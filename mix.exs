  defp json_ld_by_category(category, excluded) do
    @json_ld_categories
    |> Enum.filter(fn {_mod, cat} -> cat == category end)
    |> Enum.map(fn {mod, _cat} -> mod end)
    |> Enum.reject(&MapSet.member?(excluded, &1))
    |> Enum.sort()
  end

